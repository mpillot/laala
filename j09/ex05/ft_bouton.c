int		ft_bouton(int i, int j, int k)
{
	if ((i > j && i < k) || (i > k && i < j))
		return (i);
	if ((k > j && k < i) || (k > i && k < i))
		return (k);
	if ((j > k && j < i) || (j > i && j < k))
		return (j);
	return (0);
}
